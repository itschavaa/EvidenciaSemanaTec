# Draws a blue square in the given position
def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


# Receives a point (vector) and returns a pair por numbers that translates the point position into a index of a 20 x 20
# grid
def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


# Checks if a point is valid (not a wall) for movement
def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


# Draws the entire world (board)
def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


# Moves Pacman and the ghosts
def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])  # Updates the score
    clear()  # Clears the screen

    # Moves Pacman if the direction is valid
    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)
    # If there's food, it collects it and adds points
    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')  # Draws Pacman

    # Moves the ghosts
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')  # Draws the ghost

    update()  # Refreshes the screen

    # Checks for collision between Pacman and ghosts
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)  # Calls move() again after 100 ms


# Changes Pacman's direction if valid
def change(x, y):
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


# Sets up the game window
setup(420, 420, 370, 0)
